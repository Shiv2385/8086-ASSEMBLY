ENDM

DATA SEGMENT
 ARRAY DB 03H,01H,10H,32H,20H,30H
 LEN1 EQU ($-ARRAY)
 KEY DB 32H
 STRING1 DB 0AH,0DH,"KEY FOUND$"
 STRING2 DB 0AH,0DH,"KEY NOT FOUND$"
DATA ENDS

CODE SEGMENT
 ASSUME CS:CODE,DS:DATA
START:
 MOV AX,DATA
 MOV DS,AX

 LEA SI,ARRAY
 MOV CX,LEN1
 MOV BL,KEY
NEXT: MOV AL,[SI]
 CMP AL,BL
 JE FOUND
 INC SI
 DEC CX
 JNZ NEXT
 PRINT STRING2
 JMP EXIT
FOUND:
 PRINT STRING1
EXIT:
 MOV AH,4CH
 INT 21H
CODE ENDS
END START
