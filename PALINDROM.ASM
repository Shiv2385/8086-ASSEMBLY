DATA SEGMENT
 STR1 DB 20 DUP(' '),'$'
 MSG DB "ENTER THE STRING : $"
 PAL DB 0DH,0AH,"PALINDROME$"
 NOTPAL DB 0DH,0AH,"NOT PALINDROME$"
DATA ENDS

CODE SEGMENT
 ASSUME CS:CODE,DS:DATA
START:
 MOV AX,DATA
 MOV DS,AX

 MOV AH, 09H
 LEA DX, MSG
 INT 21H
 LEA SI,STR1
 LEA DI,STR1
 MOV AH,01H

TAKEINPUT:
 INT 21H
 CMP AL,0DH
 JE TERMINATE
 MOV [SI],AL
 INC SI
 JMP TAKEINPUT

TERMINATE: 
 MOV AL,'$'
 MOV [SI],AL

DOTHIS:
 DEC SI
 MOV AL,[SI]
 CMP [DI],AL
 JNE NOTPALINDROME
 INC DI
 CMP DI,SI
 JL DOTHIS

PALINDROME:
 MOV AH,09H
 LEA DX,PAL
 INT 21H
 JMP THEENDBEGINS

NOTPALINDROME:
 MOV AH,09H
 LEA DX,NOTPAL
 INT 21H

THEENDBEGINS:
 MOV AH,4CH 
 INT 21H
CODE ENDS
END START
