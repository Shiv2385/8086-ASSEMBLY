DATA SEGMENT
 MSG1 DB 0AH,0DH,'ENTER FIRST NUMBER : $',0AH,0DH
 MSG2 DB 0AH,0DH,'ENTER SECOND NUMBER : $',0AH,0DH
 MSG3 DB 0AH,0DH,'DIFFERENCE IS : $',0AH,0DH
 MSG4 DB 0AH,0DH,'DIFFERENCE IS : -$',0AH,0DH
DATA ENDS

CODE SEGMENT
 ASSUME CS:CODE, DS:DATA
START:
 MOV AX,DATA
 MOV DS, AX

 LEA DX, MSG1
 MOV AH, 09H
 INT 21H
 MOV AH, 01H
 INT 21H
 SUB AL, 30H
 MOV BL, AL

 LEA DX, MSG2
 MOV AH, 09H
 INT 21H
 MOV AH, 01H
 INT 21H
 SUB AL, 30H
 MOV CL, AL

 MOV AL, BL
 CMP AL, CL
 JL NEGATIVE

 SUB AL, CL
 ADD AL, 30H

 LEA DX, MSG3
 MOV AH, 09H
 INT 21H
 MOV DL, AL
 MOV AH, 02H
 INT 21H
 JMP EXIT


NEGATIVE:
 MOV AL, CL
 SUB AL, BL
 ADD AL, 30H

 LEA DX, MSG4
 MOV AH, 09H
 INT 21H
 MOV DL, AL
 MOV AH, 02H
 INT 21H

EXIT:
 MOV AH, 4CH
 INT 21H
CODE ENDS
END START
